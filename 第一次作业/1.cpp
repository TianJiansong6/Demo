#include <stdio.h>
#include <math.h>
int main(void)
{
	int a,b,c=24,p,q;
	printf("请输入需要被转换的二进制数\n");
	scanf("%d",&a);
	
	b=a/10000000*pow(2,7)+a/1000000%10*pow(2,6)+a/100000%10*pow(2,5)+a/10000%10*pow(2,4)+a/1000%10*pow(2,3)+a/100%10*pow(2,2)+a/10%10*2+a%10;
	p=b+c;
	q=p/(2*2*2*2*2*2*2)%2*pow(10,7)+p/(2*2*2*2*2*2)%2*pow(10,6)+p/(2*2*2*2*2)%2*pow(10,5)+p/(2*2*2*2)%2*pow(10,4)+p/(2*2*2)%2*pow(10,3)+p/(2*2)%2*10+p/2%2*10+p%2;
	
	printf("经过复杂的运算得到所需数%08d\n",q);
	
	return 0;
}
